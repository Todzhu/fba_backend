-- 分析工具表建表SQL
CREATE TABLE `analysis_tool` (
  `id` BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  `name` VARCHAR(64) NOT NULL COMMENT '工具名称',
  `description` TEXT COMMENT '工具描述',
  `icon` VARCHAR(32) COMMENT '图标名',
  `icon_bg` VARCHAR(16) COMMENT '图标背景色',
  `icon_color` VARCHAR(16) COMMENT '图标颜色',
  `category` VARCHAR(32) COMMENT '组学分类',
  `type` VARCHAR(32) COMMENT '功能分类',
  `views` INT DEFAULT 0 COMMENT '浏览量',
  `likes` INT DEFAULT 0 COMMENT '点赞数',
  `is_favorite` BOOLEAN DEFAULT FALSE COMMENT '是否收藏',
  `created_time` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_time` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT='分析工具表';

